package Array.Questions;

public class WrapperClass {
    public static void main(String[] args) {
        int p = 12;
        Integer wrapperValue1 = Integer.valueOf(p);
        System.out.println(wrapperValue1);

        Integer wrapperValue2 = 120;
        int i = wrapperValue2;
        System.out.println(i);
    }
}
